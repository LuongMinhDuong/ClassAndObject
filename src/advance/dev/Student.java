package advance.dev;

public class Student {
	public String name;
	public int age;
	public String address;
	public int number;
	public float markAverage;
	public Student(String name, int age, String address, String number, float markAverage) {
		super();
		this.name = name;
		this.age = age;
		this.address = address;
		this.number = number;
		this.markAverage = markAverage;
	}
}
